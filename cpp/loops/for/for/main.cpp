#include <iostream>
using namespace std;

int main() {
    
    int i;
    for (i=1; i<=20; i++) {
        cout << i << " ";
    }
    cout << endl;
    
    int j;
    for (j=0; j<=20; j+=2){
        cout << j << " ";
    }
    cout << endl;
    
    int k;
    for (k=1; k<20; k+=2) {
        cout << k <<" ";
    }
    cout << endl;
    
    return 0;
}
